#!/usr/bin/env node
/**
 * Fetches OSS hub roadmap from opensource-contributions and writes
 * src/data/oss-stacks.generated.ts for the portfolio site.
 */
import { writeFileSync, readFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const outPath = join(root, "src/data/oss-stacks.generated.ts");
const snapshotPath = join(root, "src/data/oss-contributions.snapshot.json");
const OSS_JSON_URL =
  "https://raw.githubusercontent.com/AsifAd/opensource-contributions/main/docs/assets/data/contributions.json";
const OSS_HUB = "https://asifad.github.io/opensource-contributions/";

const STATUS_MAP = {
  active: { status: "active", statusLabel: "Active" },
  planned: { status: "planned", statusLabel: "Planned" },
  placeholder: { status: "exploring", statusLabel: "Exploring" },
};

async function loadContributions() {
  try {
    const res = await fetch(OSS_JSON_URL, { signal: AbortSignal.timeout(15_000) });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const data = await res.json();
    writeFileSync(snapshotPath, JSON.stringify(data, null, 2) + "\n");
    console.log("sync-oss-data: fetched live contributions.json");
    return data;
  } catch (err) {
    if (!existsSync(snapshotPath)) {
      throw new Error(`sync-oss-data: fetch failed and no snapshot: ${err.message}`);
    }
    console.warn(`sync-oss-data: using snapshot (${err.message})`);
    return JSON.parse(readFileSync(snapshotPath, "utf-8"));
  }
}

function mapRoadmap(roadmap) {
  return roadmap.map((r) => {
    const mapped = STATUS_MAP[r.status] ?? STATUS_MAP.placeholder;
    const openPRs = typeof r.openPRs === "number" ? r.openPRs : 0;
    const prNote = openPRs > 0 ? ` · ${openPRs} open PR${openPRs !== 1 ? "s" : ""}` : "";
    return {
      tech: r.tech,
      name: r.label,
      status: mapped.status,
      statusLabel: mapped.statusLabel,
      focus: r.nextUp,
      openPRs,
      hubLink: `${OSS_HUB}#roadmap-${r.tech}`,
      prNote,
    };
  });
}

const data = await loadContributions();
const stacks = mapRoadmap(data.roadmap ?? []);

const file = `/* eslint-disable */
// AUTO-GENERATED by scripts/sync-oss-data.mjs — do not edit by hand
// Source: ${OSS_JSON_URL}

export type GeneratedOpenSourceStack = {
  tech: string;
  name: string;
  status: "active" | "planned" | "exploring";
  statusLabel: string;
  focus: string;
  openPRs: number;
  hubLink: string;
  prNote: string;
};

export const openSourceStacks: GeneratedOpenSourceStack[] = ${JSON.stringify(stacks, null, 2)};
`;

writeFileSync(outPath, file);
console.log(`sync-oss-data: wrote ${stacks.length} stacks → ${outPath}`);
